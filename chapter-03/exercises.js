// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: takes in two numbers
//O: the minimum of the two numbers

function min(numOne, numTwo) {
  return Math.min(numOne, numTwo);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: a positive whole number
//O: a boolean
//Notes: the function should take a number and return a boolean that measures if it is even or not
function isEven(number) {
  //base case
  if (number % 2 !== 0){
    return false;
  }
  //recursive case
  if (number % 2 === 0){
    return true;
  }
  isEven(number - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: string, character
//O: the amount of times the character is found in the string
function countChars(string, char) {
  let count = 0;
  //loop through the string
  for (let i = 0; i < string.length; i++){
    if (string[i].includes(char)){
      count++
    }
  }
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: string
//O: number that indicates how many uppercase B characters are in the string
//Notes: we can iterate through the string with a for loop
function countBs(string) {
  //make an empty holder variable
  let count = 0
  //loop through each character in the string
  for (let i = 0; i < string.length; i++){
    //if the current character in the string equals B, then increment the count to the next xharacter 
    if (string[i] === "B"){
        count++;
    }
    //return the total count of B characters
  }
  return count;
}

