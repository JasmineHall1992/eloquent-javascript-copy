// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: a positive whole number
//O: a boolean
//Notes: the function should take a number and return a boolean that measures if it is even or not
function isEven(number) {
  //base case
  if (number % 2 !== 0){
    return false;
  }
  //recursive case
  if (number % 2 === 0){
    return true;
  }
  isEven(number - 2);
}

